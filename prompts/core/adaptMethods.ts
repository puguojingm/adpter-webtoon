- 破镜重圆、宠溺反差、吃醋争风、生离死别

**悬疑/推理**：
- 凶手曝光、真相反转、证据发现、关键线索
- 危机时刻、死亡威胁、时间倒计时、最后一搏

**科幻/末世**：
- 异能觉醒、丧尸危机、基地争夺、黑科技展示
- 人性考验、背叛求生、团队对抗、终极一战

**重生复仇**：
- 先知剧情、改变命运、打脸前世仇敌、提前布局
- 夺取机缘、避开危险、身份反转、复仇成功

